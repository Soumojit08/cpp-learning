// Variables and data types
#include <iostream>
using namespace std;

int main()
{
    int x;      // Declaration of variable named x
    x = 5;      // initialization of x
    int y = 10; // declare and inititialize in both same line
    // also can do int x=1, y=9;
    cout << x + y << endl;
    // we can change value of variables like
    x = 20;
    cout << x + y << endl;
    return 0;
}